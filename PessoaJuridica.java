
public class PessoaJuridica extends Pessoa {

	private int CNPJ;
	private int Razão_Social;
	private int Renda;
	private int IM;
	private int IE;
	private int Capital_Social;

	public int getCNPJ() {
		return CNPJ;
	}

	public void setCNPJ(int cNPJ) {
		CNPJ = cNPJ;
	}

	public int getRazão_Social() {
		return Razão_Social;
	}

	public void setRazão_Social(int razão_Social) {
		Razão_Social = razão_Social;
	}

	public int getRenda() {
		return Renda;
	}

	public void setRenda(int renda) {
		Renda = renda;
	}

	public int getIM() {
		return IM;
	}

	public void setIM(int iM) {
		IM = iM;
	}

	public int getIE() {
		return IE;
	}

	public void setIE(int iE) {
		IE = iE;
	}

	public int getCapital_Social() {
		return Capital_Social;
	}

	public void setCapital_Social(int capital_Social) {
		Capital_Social = capital_Social;
	}

}
